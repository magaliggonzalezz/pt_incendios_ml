export class Incendio {
  constructor({ fecha, ubicacion, superficie, causa, region, estado, municipio, fuente }) {
    this.fecha = fecha;
    this.ubicacion = ubicacion;
    this.superficie = superficie;
    this.causa = causa;
    this.region = region;
    this.estado = estado;
    this.municipio = municipio;
    this.fuente = fuente;
  }
}